package main

import "fmt"

func main() {
	trees := slopeTreeCounter(1, 1)
	trees *= slopeTreeCounter(1, 3)
	trees *= slopeTreeCounter(1, 5)
	trees *= slopeTreeCounter(1, 7)
	trees *= slopeTreeCounter(2, 1)

	fmt.Println("Prod Trees:", trees)

}

func slopeTreeCounter(startRow, startCol int) int {
	row := startRow
	col := startCol
	//colInc := 3
	end := len(input) - 1
	trees := 0
	for row <= end {
		currentRow := []rune(input[row])
		currentPos := currentRow[col]
		if currentPos == '#' {
			trees++
		}
		col = (col + startCol) % len(currentRow)
		row += startRow
	}
	fmt.Printf("(%d,%d)Trees: %d\n", startRow, startCol, trees)
	return trees
}

